#----------variables------#--En Python, las variables se utilizan para almacenar valores que puedes usar más adelante entu codigo. las variables
# pueden contener diferentes tipos de datos como numericos,textos(cadenas),entre otros.
#Cómo se usan las variables en Python:

#[1]-Declaración de variables: No es necesario especificar el tipo de la variable. Python lo determina automáticamente en función 
# del valor que le asignes.

nombre = "Juan"  # Una cadena (texto)
edad = 25        # Un número entero
altura = 1.75    # Un número decimal (flotante)

#[2]-Uso de las variables: Una vez que has definido una variable, puedes usarla en cualquier parte de tu código para hacer cálculos
#, mostrar valores o cualquier otra operación.

# Imprimir el valor de las variables
print(nombre)  # Salida: Juan
print(edad)    # Salida: 25

# Operaciones con variables
nueva_edad = edad + 5
print(nueva_edad)  # Salida: 30

